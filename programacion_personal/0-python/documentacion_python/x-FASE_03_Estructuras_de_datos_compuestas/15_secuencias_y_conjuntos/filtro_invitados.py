# Tienes dos listas de invitados: `invitados_boda = {"Ana", "Luis", "Juan"}` e `invitados_cena = {"Juan", "Marta", "Pedro"}`.
#  Usa el operador `&` (intersección) para descubrir quién está invitado a **ambos** eventos.

invitados_boda = {"Ana", "Luis", "Juan"}
invitados_cena = {"Juan", "Marta", "Pedro"}

ambos = invitados_boda & invitados_cena 

print(ambos)

